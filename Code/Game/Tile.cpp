#include "Game/Tile.hpp"

//------------------------------------------------------------------------------------------------------------------------------
Tile::Tile()
{

}

//------------------------------------------------------------------------------------------------------------------------------
Tile::Tile(bool isWalkable, unsigned int tileID, const IntVec2& mapDimensions)
{
	m_isWalkable = isWalkable;
	m_tileID = tileID;

	m_tileCoordinates.y = m_tileID / mapDimensions.x;
	m_tileCoordinates.x = m_tileID % mapDimensions.x;
}

//------------------------------------------------------------------------------------------------------------------------------
IntVec2 Tile::GetTileCoordinates()
{
	return m_tileCoordinates;
}

//------------------------------------------------------------------------------------------------------------------------------
int Tile::GetTileIndex()
{
	return m_tileID;
}

//------------------------------------------------------------------------------------------------------------------------------
void Tile::SetTileType(eTileType tileType)
{
	m_tileType = tileType;
}

//------------------------------------------------------------------------------------------------------------------------------
AABB2 Tile::GetTileDimensions() const
{
	return m_dimensions;
}

//------------------------------------------------------------------------------------------------------------------------------
void Tile::SetTileColor(const Rgba& color)
{
	m_tileColor = color;
}

//------------------------------------------------------------------------------------------------------------------------------
const Rgba& Tile::GetTileColor() const
{
	return m_tileColor;
}

//------------------------------------------------------------------------------------------------------------------------------
Tile::~Tile()
{

}

